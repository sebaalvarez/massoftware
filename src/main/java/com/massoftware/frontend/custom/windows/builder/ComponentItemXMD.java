package com.massoftware.frontend.custom.windows.builder;

public class ComponentItemXMD {

	private String alignment;
	private ComponentXMD component;
	private int row;
	private int column;

	public String getAlignment() {
		return alignment;
	}

	public void setAlignment(String alignment) {
		this.alignment = alignment;
	}

	public ComponentXMD getComponent() {
		return component;
	}

	public void setComponent(ComponentXMD component) {
		this.component = component;
	}

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getColumn() {
		return column;
	}

	public void setColumn(int column) {
		this.column = column;
	}

}
